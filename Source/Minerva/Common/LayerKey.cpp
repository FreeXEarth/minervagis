///////////////////////////////////////////////////////////////////////////////
//
//  Key for a layer.
//
///////////////////////////////////////////////////////////////////////////////

#include "Minerva/Common/LayerKey.h"

using namespace Minerva::Common;


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

LayerKey::LayerKey ( const std::string& name, std::size_t id ) : BaseClass(),
  _name ( name ),
  _id ( id )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

LayerKey::~LayerKey()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the name.
//
///////////////////////////////////////////////////////////////////////////////

const std::string& LayerKey::name() const
{
  return _name;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Get the id.
//
///////////////////////////////////////////////////////////////////////////////

std::size_t LayerKey::id() const
{
  return _id;
}
