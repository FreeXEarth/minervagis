#include "MinervaQt/Plugins/QtCustomGeometry/QtCustomGeometryComponent.h"
#include "MinervaQt/Plugins/QtCustomGeometry/AddGeometryWidget.h"

#include "Usul/Components/Factory.h"

USUL_DECLARE_COMPONENT_FACTORY ( QtCustomGeometryComponent );
USUL_IMPLEMENT_IUNKNOWN_MEMBERS ( QtCustomGeometryComponent, QtCustomGeometryComponent::BaseClass );


///////////////////////////////////////////////////////////////////////////////
//
// Constructor
//
///////////////////////////////////////////////////////////////////////////////

QtCustomGeometryComponent::QtCustomGeometryComponent() : BaseClass(),
  _widget ( 0x0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
// Destructor
//
///////////////////////////////////////////////////////////////////////////////

QtCustomGeometryComponent::~QtCustomGeometryComponent() 
{
  // Qt Deletes the widget
}


///////////////////////////////////////////////////////////////////////////////
//
//  Query for the interface.
//
///////////////////////////////////////////////////////////////////////////////

Usul::Interfaces::IUnknown *QtCustomGeometryComponent::queryInterface ( unsigned long iid )
{
  switch ( iid )
  {
  case Usul::Interfaces::IUnknown::IID:
  case Usul::Interfaces::IPlugin::IID:
    return static_cast < Usul::Interfaces::IPlugin* > ( this );
  case Minerva::Common::ILayerAddGUIQt::IID:
    return static_cast < Minerva::Common::ILayerAddGUIQt * > ( this );
  default:
    return 0x0;
  }
}


///////////////////////////////////////////////////////////////////////////////
//
//  Usul::Interfaces::IPlugin implementation
//
///////////////////////////////////////////////////////////////////////////////

std::string QtCustomGeometryComponent::getPluginName() const 
{
  return "Qt Custom Geometry GUI";
}


///////////////////////////////////////////////////////////////////////////////
//
//  Return the widget to add the gui.
//
///////////////////////////////////////////////////////////////////////////////

QWidget* QtCustomGeometryComponent::layerAddGUI()
{
  _widget = new AddGeometryWidget;
  return _widget;
}


///////////////////////////////////////////////////////////////////////////////
//
//  The name of the layer type.
//
///////////////////////////////////////////////////////////////////////////////

std::string QtCustomGeometryComponent::name() const 
{
  return "Custom Geometry";
}


///////////////////////////////////////////////////////////////////////////////
//
//  Apply.
//
///////////////////////////////////////////////////////////////////////////////

void QtCustomGeometryComponent::apply ( Minerva::Core::Data::Feature* parent, DataLoadedCallback dataLoadedCallback )
{
  if ( 0x0 != _widget )
    _widget->apply ( parent );

  _widget = 0x0;
}
